console.log("For Loop=========================");

//For Loop

for(i=10; i>=1; i--){
    console.log(i);
}



console.log("While Loop=========================");


//While Loop

var i = 10;
while(i>=1){
    console.log(i);
    i--;
}

console.log("Do ..While Loop=========================");


// Do ..While Loop

var i = 10;
do {
    console.log(i);
    i--;
}while(i>=1);

